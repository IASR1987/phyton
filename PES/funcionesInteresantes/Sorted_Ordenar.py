#ordenar lista numero
numeros = [5, 2, 9, 1, 5, 6]
numeros_ordenados = sorted(numeros)
print(numeros_ordenados)  # Salida: [1, 2, 5, 5, 6, 9]

#orden descendente
numeros = [5, 2, 9, 1, 5, 6]
numeros_descendentes = sorted(numeros, reverse=True)
print(numeros_descendentes)  # Salida: [9, 6, 5, 5, 2, 1]

#ordenar lista
frutas = ["banana", "manzana", "naranja", "kiwi"]
frutas_ordenadas = sorted(frutas)
print(frutas_ordenadas)  # Salida: ['banana', 'kiwi', 'manzana', 'naranja']

# Ordenar una cadena de caracteres
cadena = "banana"
caracteres_ordenados = sorted(cadena)
print(caracteres_ordenados)  # Salida: ['a', 'a', 'a', 'b', 'n', 'n']


